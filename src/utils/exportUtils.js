// ─── Formatters ───────────────────────────────────────────────────────────────

export function fmtH(s) {
  const h = (s || 0) / 3600;
  return h >= 10 ? `${h.toFixed(0)}h` : `${h.toFixed(1)}h`;
}

export function fmtDuration(s) {
  if (!s || s < 0) return '0m';
  const h = Math.floor(s / 3600), m = Math.floor((s % 3600) / 60);
  if (h === 0) return `${m}m`;
  if (m === 0) return `${h}h`;
  return `${h}h ${m}m`;
}

export function fmtMoney(v) {
  if (!v || v === 0) return '—';
  return `$${Number(v).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

export function fmtPct(part, total) {
  if (!total) return '0%';
  return `${Math.round(((part || 0) / total) * 100)}%`;
}

export function fmtDate(ts) {
  if (!ts) return '—';
  return new Date(ts * 1000).toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: 'numeric' });
}

export function fmtNow() {
  return new Date().toLocaleString('en-US', {
    year: 'numeric', month: 'short', day: 'numeric',
    hour: '2-digit', minute: '2-digit',
  });
}

export function fmtDateRange(fromTs, toTs) {
  const opts = { month: 'short', day: 'numeric', year: 'numeric' };
  return `${new Date(fromTs * 1000).toLocaleDateString('en-US', opts)} – ${new Date(toTs * 1000).toLocaleDateString('en-US', opts)}`;
}

// ─── CSV builder ──────────────────────────────────────────────────────────────

/** Wrap a value in quotes, escaping internal quotes — safe for any cell value. */
function cell(v) {
  if (v == null) return '""';
  const s = String(v);
  return `"${s.replace(/"/g, '""')}"`;
}

/**
 * Build a professional CSV string with metadata header and multiple sections.
 *
 * @param {string}   reportTitle  — e.g. "Flow Ledger — Focus Analytics"
 * @param {object}   meta         — { dateRange?, period?, filters?, generatedBy? }
 * @param {Section[]} sections    — see Section shape below
 *
 * Section shape:
 *   { title, subtitle?, kpis?:[{label,value}], headers?:string[], rows?:any[][], summary?:[string,string][] }
 */
export function buildCSV(reportTitle, meta, sections) {
  const lines = [];

  // ── Report header block ────────────────────────────────────────────────────
  lines.push(cell(reportTitle));
  lines.push(`${cell('Flow Ledger')},${cell('Professional Work Analytics Report')}`);
  lines.push(`${cell('Generated')},${cell(fmtNow())}`);
  if (meta.dateRange)   lines.push(`${cell('Date Range')},${cell(meta.dateRange)}`);
  if (meta.period)      lines.push(`${cell('Period')},${cell(meta.period)}`);
  if (meta.filters)     lines.push(`${cell('Active Filters')},${cell(meta.filters)}`);
  if (meta.generatedBy) lines.push(`${cell('Generated By')},${cell(meta.generatedBy)}`);
  lines.push('');

  // ── Sections ───────────────────────────────────────────────────────────────
  for (const sec of sections) {
    // Section title separator
    lines.push(cell(`═══ ${sec.title.toUpperCase()} ═══`));
    if (sec.subtitle) lines.push(`${cell('')},${cell(sec.subtitle)}`);
    lines.push('');

    // KPI summary rows (before the main table)
    if (sec.kpis?.length) {
      lines.push(cell('KEY METRICS'));
      for (const kpi of sec.kpis) {
        lines.push(`${cell(kpi.label)},${cell(kpi.value)}`);
      }
      lines.push('');
    }

    // Column headers + data rows
    if (sec.headers?.length) {
      lines.push(sec.headers.map(cell).join(','));
    }
    for (const row of (sec.rows || [])) {
      lines.push(row.map(cell).join(','));
    }

    // Summary key-value block (after the table)
    if (sec.summary?.length) {
      lines.push('');
      lines.push(cell('SECTION SUMMARY'));
      for (const [k, v] of sec.summary) {
        lines.push(`${cell(k)},${cell(v)}`);
      }
    }

    lines.push('');
    lines.push('');
  }

  // ── Footer ─────────────────────────────────────────────────────────────────
  lines.push(cell('─── END OF REPORT ───'));
  lines.push(`${cell('Confidential')},${cell('Generated by Flow Ledger · ' + fmtNow())}`);

  return lines.join('\n');
}

// ─── Download trigger ─────────────────────────────────────────────────────────

export function triggerDownload(content, filename, mimeType = 'text/csv;charset=utf-8;') {
  // BOM makes Excel on Windows open UTF-8 CSV correctly
  const bom  = mimeType.includes('csv') ? '﻿' : '';
  const blob = new Blob([bom + content], { type: mimeType });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href     = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

export function exportAsCSV(reportTitle, meta, sections, filename) {
  const csv = buildCSV(reportTitle, meta, sections);
  triggerDownload(csv, filename || `flow-ledger-${new Date().toISOString().split('T')[0]}.csv`);
}

