/**
 * Build a print-ready HTML document string.
 *
 * @param {string}   reportTitle
 * @param {object}   meta          — { dateRange, period, filters?, generatedBy? }
 * @param {Section[]} sections
 */
export function buildPrintHTML(reportTitle, meta, sections) {
  // ── Metadata bar ──────────────────────────────────────────────────────────
  const metaEntries = Object.entries(meta).filter(([, v]) => v != null && v !== '');
  const metaHtml = metaEntries.map(([k, v]) => `
    <div class="rpt-meta-item">
      <div class="rpt-meta-label">${k.replace(/([A-Z])/g, ' $1').replace(/^./, s => s.toUpperCase())}</div>
      <div class="rpt-meta-value">${v}</div>
    </div>`).join('');

  // ── Sections ──────────────────────────────────────────────────────────────
  const sectionsHtml = sections.map(sec => {
    let inner = '';

    if (sec.kpis?.length) {
      inner += `<div class="rpt-kpi-row">
        ${sec.kpis.map(k => `
          <div class="rpt-kpi">
            <div class="rpt-kpi-value">${k.value}</div>
            <div class="rpt-kpi-label">${k.label}</div>
          </div>`).join('')}
      </div>`;
    }

    if (sec.headers?.length && sec.rows?.length) {
      const displayRows = sec.rows.slice(0, 500);
      inner += `<table>
        <thead><tr>${sec.headers.map(h => `<th>${h}</th>`).join('')}</tr></thead>
        <tbody>${displayRows.map(row =>
          `<tr>${row.map(c => `<td>${c ?? '—'}</td>`).join('')}</tr>`
        ).join('')}</tbody>
      </table>`;
      if (sec.rows.length > 500) {
        inner += `<p style="font-size:10px;color:#94a3b8;margin-top:8px">
          Showing 500 of ${sec.rows.length} rows — export CSV for full dataset.
        </p>`;
      }
    }

    if (sec.summary?.length) {
      inner += `<table class="rpt-summary-table">
        <tbody>${sec.summary.map(([k, v]) => `<tr><td>${k}</td><td>${v}</td></tr>`).join('')}</tbody>
      </table>`;
    }

    return `
      <div class="rpt-section">
        <div class="rpt-section-header">
          <div class="rpt-section-title">${sec.title}</div>
          ${sec.subtitle ? `<div class="rpt-section-sub">${sec.subtitle}</div>` : ''}
        </div>
        ${inner}
      </div>`;
  }).join('');

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width,initial-scale=1">
  <title>${reportTitle}</title>
  <style>${PRINT_CSS}</style>
</head>
<body>
  <div class="rpt-header">
    <div class="rpt-brand">Flow Ledger · Work Analytics</div>
    <h1 class="rpt-title">${reportTitle}</h1>
    <p class="rpt-subtitle">Professional productivity report · Confidential</p>
  </div>
  <div class="rpt-meta">${metaHtml}</div>
  ${sectionsHtml}
  <div class="rpt-footer">
    <span><strong>Flow Ledger</strong> — Confidential &amp; Proprietary</span>
    <span>Generated ${fmtNow()}</span>
  </div>
  <script>window.onload = () => { setTimeout(() => window.print(), 500); }</script>
</body>
</html>`;
}

export function exportAsPDF(reportTitle, meta, sections) {
  const html = buildPrintHTML(reportTitle, meta, sections);
  const win  = window.open('', '_blank', 'width=960,height=720');
  if (!win) {
    // Fallback: insert into current page in a hidden iframe
    alert('Please allow pop-ups to export PDF, or use CSV export instead.');
    return;
  }
  win.document.write(html);
  win.document.close();
}
