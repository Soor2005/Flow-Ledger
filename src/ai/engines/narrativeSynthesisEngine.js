function buildContextNote(reasoning) {
  const { behaviorProfile, compressed, client, sessionDurationMins } = reasoning;
  const { isDeepWork, productivityState } = behaviorProfile || {};

  // Prefer the actual session duration (passed from generateSessionRecap).
  // Fall back to compressed active time only if session duration isn't provided.
  const durationMins = sessionDurationMins || compressed?.totalActiveMins || 0;

  let note = '';

  if (isDeepWork && durationMins >= 60) {
    note = `Deep focus — ${Math.round(durationMins)} min of uninterrupted work.`;
  } else if (productivityState === 'exploratory' && durationMins >= 20) {
    note = `${Math.round(durationMins)}-min research and exploration session.`;
  } else if (durationMins >= 15) {
    note = `${Math.round(durationMins)}-min session.`;
  }

  if (client?.name) {
    note = note ? `${note} Client: ${client.name}.` : `Client: ${client.name}.`;
  }

  return note;
}

function synthesizeDescription(reasoning, generatedTitle) {
  // Extract title subject (strip leading verb)
  const titleSubject = generatedTitle
    ? generatedTitle
        .replace(/^(Implementing|Building|Developing|Engineering|Creating|Constructing|Debugging|Fixing|Resolving|Troubleshooting|Diagnosing|Investigating|Analyzing|Exploring|Evaluating|Designing|Crafting|Refining|Prototyping|Wireframing|Planning|Architecting|Scoping|Mapping\s+out|Structuring|Refactoring|Optimizing|Improving|Restructuring|Documenting|Writing|Drafting|Authoring|Testing|Validating|Verifying|Deploying|Releasing|Shipping|Reviewing|Auditing|Inspecting)\s+/i, '')
        .trim()
    : null;

  // Build the outcome narrative (replaces the old clause-list approach)
  const narrative = buildOutcomeNarrative(reasoning, titleSubject);

  // Append session context note
  const contextNote = buildContextNote(reasoning);
  const description = [narrative, contextNote].filter(Boolean).join(' ').trim();

  return { description, confidence: reasoning.overallConfidence || 0.75 };
}

// ─── Productivity Note Synthesis ──────────────────────────────────────────────

function synthesizeProductivityNote(reasoning) {
  const { behaviorProfile, compressed, workflowState } = reasoning;
  const { isDeepWork, isSustainedWork, productivityState, workMode, flowState } = behaviorProfile || {};
  const durationMins = compressed?.totalActiveMins || 0;
  const contextSwitches = compressed?.contextSwitches || 0;

  const dur = Math.round(durationMins);
  const activityLabel = workflowState?.activitySummary || 'focused work';

  if (isDeepWork && dur >= 90) {
    return `Deep focus — ${dur} min of sustained ${activityLabel} with minimal interruptions.`;
  }
  if (isDeepWork && dur >= 60) {
    return `Strong focus session — ${dur} min of ${activityLabel}.`;
  }
  if (isSustainedWork) {
    return `Productive ${dur}-min session of ${activityLabel}.`;
  }
  if (productivityState === 'exploratory') {
    return `Exploratory ${dur}-min session — research and investigation with ${contextSwitches} context switches.`;
  }
  if (contextSwitches > 25) {
    return `Fragmented ${dur}-min session (${contextSwitches} switches) — consider batching similar tasks.`;
  }
  return `${dur}-min ${activityLabel} session.`;
}

// ─── Alternate Title Generation ───────────────────────────────────────────────

function generateAlternateTitles(reasoning, primaryTitle) {
  const alts = [];
  const { project } = reasoning;

  // Alternate 1: different verb, same subject
  const alt1 = synthesizeTitle(reasoning, 1);
  if (alt1 && alt1 !== primaryTitle && isQualityTitle(alt1, project)) {
    alts.push(alt1);
  }

  // Alternate 2: feature-based title (only when feature is specific enough)
  const topFeature = reasoning.featureGraph?.topFeature;
  if (topFeature?.label && topFeature.label.length >= 8) {
    const intentDef = INTENT_TYPES[reasoning.intentResult?.type || 'implementing'];
    const verb2 = (intentDef?.verbs || [])[2] || pickVerb(reasoning.workMode, 2);
    const featTitle = dedup(`${verb2} ${topFeature.label}`).slice(0, 68);
    if (featTitle !== primaryTitle && isQualityTitle(featTitle, project)) {
      alts.push(featTitle);
    }
  }

  // Alternate 3: domain-elevated title (avoid bare "verb + project name")
  if (alts.length < 2) {
    const domainHint = inferTitleDomainFallback(reasoning);
    if (domainHint) {
      const intentDef = INTENT_TYPES[reasoning.intentResult?.type || 'implementing'];
      const verb3 = (intentDef?.verbs || [])[3] || pickVerb(reasoning.workMode, 3);
      const domainTitle = dedup(`${verb3} ${domainHint}`).slice(0, 68);
      if (domainTitle !== primaryTitle && !alts.includes(domainTitle) && isQualityTitle(domainTitle, project)) {
        alts.push(domainTitle);
      }
    }
  }

  return alts.slice(0, 2);
}

// ─── Quality Guards ───────────────────────────────────────────────────────────

const VAGUE_PATTERNS = [
  /^auto\s*:/i,
  /^general$/i,
  /^work$/i,
  /^task$/i,
  /^untitled/i,
  /^session$/i,
  /^focus$/i,
  /^focus session$/i,
  /^new event$/i,
  /^scheduled work$/i,
  /^tracked$/i,
  // Single-word "Verb ProjectName" combos that carry no specificity
  /^(investigating|researching|exploring|building|developing|implementing)\s+[a-z\s]{3,20}$/i,
];

// Additional check: titles that are just "Verb <project name>" add no specificity.
// Detect them by checking if the title is exactly 2 words where word1 is a vague
// gerund and word2 is a known project/company name pattern.
const VAGUE_GERUNDS = new Set([
  'investigating', 'researching', 'exploring', 'reviewing',
]);

function isVagueTwoWordTitle(title = '', project = null) {
  const words = title.trim().split(/\s+/);
  if (words.length !== 2) return false;
  const gerund = words[0].toLowerCase();
  if (!VAGUE_GERUNDS.has(gerund)) return false;
  // If the second word is the project name, it's a vague title
  if (project?.name && words[1].toLowerCase() === project.name.toLowerCase()) return true;
  return false;
}

function isQualityTitle(title = '', project = null) {
  if (!title || title.length < 6) return false;
  if (VAGUE_PATTERNS.some(p => p.test(title.trim()))) return false;
  if (/^(claude|chatgpt|vscode|figma|chrome|auto)\s*:?$/i.test(title.trim())) return false;
  if (isVagueTwoWordTitle(title, project)) return false;
  // ── Generic subject guard ──────────────────────────────────────────────────
  if (checkTitleRejectPatterns(title).rejected) return false;
  return true;
}

// ─── Main Synthesis Function ──────────────────────────────────────────────────

/**
 * Synthesize a complete set of narrative content from contextual reasoning output.
 *
 * @param {Object} reasoning - output of contextualReasoningEngine.reason()
 * @param {Object} [existing] - { title, description } — preserved if meaningful
 * @returns {Object} synthesizedContent
 */
export function synthesize(reasoning, existing = {}) {
  const preserveTitle = hasMeaningfulExistingTitle(existing.title);
  // Only preserve descriptions that are genuinely user-written — auto-generated
  // templates get replaced with the improved outcome-driven narrative.
  const existingDesc = (existing.description || '').trim();
  const preserveDesc = existingDesc.length > 10 && !isAutoGeneratedDescription(existingDesc);

  // Title
  const titleResult = (() => {
    if (preserveTitle) {
      return { title: existing.title, confidence: 1.0, source: 'manual', alternates: [] };
    }

    const title = synthesizeTitle(reasoning, 0);
    if (!isQualityTitle(title)) {
      // Emergency fallback
      const fallback = buildFallbackTitle(reasoning);
      return { title: fallback, confidence: 0.40, source: 'fallback', alternates: [] };
    }

    const alternates = generateAlternateTitles(reasoning, title);
    const source = reasoning.compressed?.titlePhrases?.length ? 'contextual_phrase'
      : reasoning.topFeature ? 'feature_graph'
      : reasoning.primaryIntent ? 'intent_inference'
      : 'mode_fallback';

    return {
      title,
      confidence: Math.round(reasoning.overallConfidence * 100) / 100,
      source,
      alternates,
    };
  })();

  // Description
  const descResult = preserveDesc
    ? { description: existing.description, confidence: 1.0 }
    : synthesizeDescription(reasoning, titleResult.title);

  // Productivity note
  const productivityNote = synthesizeProductivityNote(reasoning);

  // Deep work label
  const deepWorkLabel = buildDeepWorkLabel(reasoning);

  return {
    title: titleResult.title,
    titleConfidence: titleResult.confidence,
    titleSource: titleResult.source,
    titleAlternates: titleResult.alternates,
    description: descResult.description,
    descriptionConfidence: descResult.confidence,
    productivityNote,
    deepWorkLabel,
    category: reasoning.primaryCategory,
    isDeepWork: reasoning.isDeepWork,
    workMode: reasoning.workMode,
    titleWasGenerated: !preserveTitle,
    descriptionWasGenerated: !preserveDesc,
    isEditable: true,
  };
}

function hasMeaningfulExistingTitle(title = '', project = null) {
  if (!title || title.length <= 2) return false;
  if (VAGUE_PATTERNS.some(p => p.test(title.trim()))) return false;
  if (isVagueTwoWordTitle(title, project)) return false;
  // Reject bare app-name titles
  if (/^(claude|chatgpt|vscode|figma|chrome|auto|notion|linear|slack)\s*:?$/i.test(title.trim())) return false;
  return true;
}

function buildFallbackTitle(reasoning) {
  const { workMode, primaryCategory, project, intentResult } = reasoning;
  const intentType = intentResult?.type || workMode?.replace('deep_', '') || 'implementing';
  const intentDef  = INTENT_TYPES[intentType] || INTENT_TYPES.implementing;
  const verb       = (intentDef.verbs || [])[0] || pickVerb(workMode, 0);

  // Attempt domain elevation from keywords first — better than just project name
  const domainHint = inferTitleDomainFallback(reasoning);
  if (domainHint) return `${verb} ${domainHint}`;

  // Keyword-synthesized subject as next best option
  const synthSubject = synthesizeSubjectFromKeywords(reasoning.compressed, intentType, project);
  if (synthSubject) return `${verb} ${synthSubject}`;

  // Category-based semantic labels — more descriptive than "Research Session"
  const catLabels = {
    development: 'Feature Implementation',
    design:      'UI Design System',
    writing:     'Technical Documentation',
    research:    'Technical Analysis',
    planning:    'Architecture Planning',
    meeting:     'Team Alignment',
  };
  return `${verb} ${catLabels[primaryCategory] || 'Product Development'}`;
}

function buildDeepWorkLabel(reasoning) {
  const { isDeepWork, behaviorProfile, compressed } = reasoning;
  const durationMins = compressed?.totalActiveMins || 0;
  const contextSwitches = compressed?.contextSwitches || 0;

  if (durationMins >= 90 && contextSwitches < 5) return 'Deep Focus';
  if (durationMins >= 60 && contextSwitches < 10) return 'Focused Work';
  if (isDeepWork) return 'Deep Work';
  if (durationMins >= 30) return 'Work Session';
  return 'Quick Session';
}

/**
 * Batch synthesize for sessions without titles.
 *
 * @param {Array} reasoningResults - array of reason() outputs
 * @param {Array} existingData - array of { title, description } per session
 */
export function synthesizeBatch(reasoningResults = [], existingData = []) {
  return reasoningResults.map((reasoning, i) =>
    synthesize(reasoning, existingData[i] || {})
  );
}
