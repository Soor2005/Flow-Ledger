// ─── Subject Extraction ───────────────────────────────────────────────────────

const LEADING_VERB_RE = /^(building|implementing|designing|creating|developing|fixing|debugging|testing|writing|researching|refactoring|reviewing|planning|improving|adding|updating|configuring|setting\s+up|working\s+on|exploring|optimizing|crafting|prototyping|engineering|deploying|drafting|validating|resolving)\s+/i;

function extractSubjectFromPhrase(phrase) {
  const match = phrase.match(LEADING_VERB_RE);
  if (match) {
    const obj = phrase.slice(match[0].length).trim();
    return { subject: capitalizeFirst(obj), verbExtracted: match[1] };
  }
  return { subject: capitalizeFirst(phrase), verbExtracted: null };
}

function capitalizeFirst(str = '') {
  return str ? str.charAt(0).toUpperCase() + str.slice(1) : '';
}

function capitalizeWords(str = '') {
  return str.replace(/\b([a-z])/g, c => c.toUpperCase());
}

function dedup(title = '') {
  const words = title.split(/\s+/);
  const result = [];
  for (let i = 0; i < words.length; i++) {
    if (i === 0 || words[i].toLowerCase() !== words[i - 1].toLowerCase()) {
      result.push(words[i]);
    }
  }
  return result.join(' ').replace(/(\b\w+\s+\w+\b)\s+\1/gi, '$1');
}

// ─── Verb Picking (per-call state, not module-level) ─────────────────────────
// NOTE: verb rotation state is passed in as a parameter to avoid module-level
// mutable state that would persist across users and sessions.

function pickVerb(workMode, position = 0, usedVerbs = []) {
  const verbs = MODE_VERB_SETS[workMode] || MODE_VERB_SETS.deep_implementation;

  for (let i = position; i < verbs.length + position; i++) {
    const verb = verbs[i % verbs.length];
    if (!usedVerbs.slice(-3).includes(verb)) {
      return verb;
    }
  }
  return verbs[position % verbs.length];
}

// ─── Semantic Compound Phrase Builders ───────────────────────────────────────
// Build compound phrases from feature combinations and workflow context.
// This is the key upgrade: instead of "verb + topSignal", we synthesize
// meaning from the RELATIONSHIP between signals.

const CONNECTING_PHRASES = [
  'and refining',
  'alongside',
  'while iterating on',
  'with focus on',
  'and improving',
  'and optimizing',
];

function buildCompoundSubject(reasoning) {
  const { featureGraph, compressed, fusedWorkblock, intentResult, workMode, project } = reasoning;
  const intentType = intentResult?.type || workMode?.replace('deep_', '') || 'implementing';

  // Priority 1: AI conversation topic — most precise signal available
  // (e.g. "Fix burnout calculation — Claude" → subject = "Burnout Calculation Fix")
  const aiTopics = compressed?.aiConversationTopics || [];
  if (aiTopics.length > 0 && aiTopics[0].topic?.length >= 6) {
    const { subject } = extractSubjectFromPhrase(aiTopics[0].topic);
    if (!isVagueSubject(subject) && !isProjectNameOnly(subject, project)) {
      return { subject: capitalizeFirst(subject), source: 'ai_topic', confidence: 0.94 };
    }
  }

  // Priority 1b: Dominant workflow label — the workflow that contributed the most
  // time in the session, filtered by the workflowDominanceEngine. Use it when the
  // dominance score is high and it adds specificity beyond the project name alone.
  const dominantLabel = compressed?.dominantWorkflowLabel;
  const dominanceScore = compressed?.dominanceScore || 0;
  if (dominantLabel && dominantLabel.length >= 8 && dominanceScore >= 0.65
      && !isProjectNameOnly(dominantLabel, project)) {
    const { subject } = extractSubjectFromPhrase(dominantLabel);
    if (!isVagueSubject(subject)) {
      return { subject: capitalizeFirst(subject), source: 'dominant_workflow', confidence: Math.min(dominanceScore + 0.1, 0.92) };
    }
  }

  // Priority 2: fusedWorkblock provides synthesized multi-signal context
  if (fusedWorkblock?.fusedSubject && fusedWorkblock.fusedSubject.length >= 8
      && !isProjectNameOnly(fusedWorkblock.fusedSubject, project)) {
    return { subject: fusedWorkblock.fusedSubject, source: 'fusion', confidence: fusedWorkblock.fusionConfidence };
  }

  // Priority 3: best window title phrase — actual work description
  for (const p of compressed?.titlePhrases || []) {
    if (p.isAITopic) continue; // already handled above
    const { subject } = extractSubjectFromPhrase(p.phrase);
    if (subject.length >= 8 && !isVagueSubject(subject) && !isGenericSubject(subject)
        && !isUtilityApp(subject) && !isProjectNameOnly(subject, project)) {
      return { subject: capitalizeFirst(subject), source: 'title_phrase', confidence: 0.85 };
    }
  }

  // Priority 4: feature compound from fusion (two features = specific objective)
  if (fusedWorkblock?.featureCompound) {
    return { subject: fusedWorkblock.featureCompound, source: 'feature_compound', confidence: 0.80 };
  }

  // Priority 5: intent-feature phrase from ontology (Flow Ledger-specific fallback)
  const topFeatureId = featureGraph?.activeCluster?.[0]?.featureId;
  if (topFeatureId) {
    const ontologyPhrase = getIntentFeaturePhrase(intentType, topFeatureId);
    if (ontologyPhrase) {
      return { subject: ontologyPhrase, source: 'ontology_phrase', confidence: 0.72 };
    }
  }

  // Priority 6: keyword-synthesized subject (better than just the feature label)
  const synth = synthesizeSubjectFromKeywords(compressed, intentType, project);
  if (synth) return { subject: synth, source: 'keyword_synthesis', confidence: 0.65 };

  // Priority 7: top feature label
  if (featureGraph?.topFeature?.label) {
    return { subject: featureGraph.topFeature.label, source: 'feature_label', confidence: 0.60 };
  }

  // Priority 8: primary intent phrase
  if (reasoning.primaryIntent?.length >= 10) {
    return { subject: capitalizeFirst(reasoning.primaryIntent), source: 'intent', confidence: 0.50 };
  }

  return { subject: null, source: 'none', confidence: 0 };
}

// Returns true when a subject is JUST the project name (provides no extra specificity)
function isProjectNameOnly(subject, project) {
  if (!project?.name || !subject) return false;
  const subNorm = subject.toLowerCase().trim();
  const projNorm = project.name.toLowerCase().trim();
  return subNorm === projNorm || subNorm === projNorm + ' development' || subNorm === projNorm + ' features';
}

// Synthesize a semantic subject phrase from keywords when no phrase is available.
// Uses the generic keyword filter to ensure only meaningful words are used.
function synthesizeSubjectFromKeywords(compressed, intentType, project) {
  const rawKeywords = compressed?.keywords || [];
  const category = compressed?.primaryCategory || 'development';

  // Hard skip list (browser/AI tool names that pollute keywords)
  const HARD_SKIP = new Set([
    'using', 'from', 'with', 'into', 'that', 'this', 'have', 'been', 'will',
    'chrome', 'safari', 'firefox', 'edge', 'claude', 'chatgpt', 'gemini',
    'code', 'vscode', 'cursor', 'copilot',
  ]);

  // Step 1: Remove hard skips
  const afterHardSkip = rawKeywords.filter(k => !HARD_SKIP.has(k) && k.length >= 4);

  // Step 2: Remove ALL generic application terms
  // This is the critical fix: "productivity", "dashboard", "analytics", "system",
  // "module", "feature", "component", "interface" etc. are EXCLUDED here.
  const meaningful = filterGenericKeywords(afterHardSkip, { strict: false })
    .filter(k => k.length >= 5)
    .slice(0, 4);

  if (meaningful.length >= 2) {
    // Build compound phrase from top 2 non-generic keywords
    const first  = capitalizeWords(meaningful[0]);
    const second = capitalizeWords(meaningful[1]);
    const candidate = `${first} and ${second}`;
    // Verify the compound is actually specific
    if (!isGenericSubject(candidate)) return candidate;
  }

  if (meaningful.length === 1) {
    const suffix = {
      development: 'Implementation',
      design:      'Design',
      research:    'Analysis',
      planning:    'Architecture',
      writing:     'Documentation',
    }[category] || 'Development';
    const candidate = capitalizeWords(`${meaningful[0]} ${suffix}`);
    if (!isGenericSubject(candidate)) return candidate;
  }

  return null;
}

// ─── Outcome-Driven Description Builder ──────────────────────────────────────
// Builds descriptions that answer: WHAT was done + WHY + WHAT outcome was pursued.
// Apps are NEVER listed — the focus is entirely on workflow, intent, and purpose.

function buildOutcomeNarrative(reasoning, titleSubject) {
  const {
    compressed, intentResult, continuityProfile, workMode, project,
    fusedWorkblock,
  } = reasoning;

  const intentType = intentResult?.type || workMode?.replace('deep_', '') || 'implementing';
  const intentDef  = INTENT_TYPES[intentType] || INTENT_TYPES.implementing;

  // ── Past verb — chosen by intent, not by app ───────────────────────────────
  const pastVerb = intentDef.pastVerbs[0];

  // ── Primary subject — specific work description ───────────────────────────
  // Prefer: AI topic > fused subject > window title phrase > fallback
  let subject = titleSubject || null;

  // Enrich the subject if it's too short or is just the project name
  if (!subject || subject.length < 5 || isProjectNameOnly(subject, project)) {
    const aiTopics = compressed?.aiConversationTopics || [];
    if (aiTopics.length > 0 && aiTopics[0].topic?.length >= 6) {
      const { subject: aiSubj } = extractSubjectFromPhrase(aiTopics[0].topic);
      if (!isVagueSubject(aiSubj)) subject = capitalizeFirst(aiSubj);
    }
  }

  if (!subject || isVagueSubject(subject) || isGenericSubject(subject)) {
    for (const p of compressed?.titlePhrases || []) {
      const { subject: ps } = extractSubjectFromPhrase(p.phrase);
      if (ps.length >= 6 && !isVagueSubject(ps) && !isGenericSubject(ps) && !isProjectNameOnly(ps, project)) {
        subject = capitalizeFirst(ps);
        break;
      }
    }
  }

  // Expand the subject with secondary phrase context when available
  const secondPhrase = getSecondaryPhrase(compressed, subject);

  // ── Purpose clause — explains WHY the work was done ───────────────────────
  const purposeClause = intentDef.purpose
    ? intentDef.purpose(subject, project?.name)
    : null;

  // ── Continuity clause — situates the work in ongoing context ──────────────
  let continuityClause = null;
  if (continuityProfile?.isContinuingWork) {
    const obj = continuityProfile?.activeObjective?.description;
    continuityClause = obj
      ? `as part of ongoing work on ${obj}`
      : 'as part of ongoing implementation';
  }

  // ── Assemble the sentence ──────────────────────────────────────────────────
  // Pattern: "[PastVerb] [subject][, expanding into secondPhrase], [purpose clause]."
  // Never list apps. Always lead with what was done and why.

  const subjectStr = subject
    ? (subject.charAt(0).toLowerCase() + subject.slice(1))
    : (project?.name ? project.name.toLowerCase() : 'the work');

  let body = `${pastVerb} ${subjectStr}`;

  // Add secondary context when it genuinely extends the primary (no overlap)
  if (secondPhrase) {
    const bodyNorm = body.toLowerCase();
    const secNorm  = secondPhrase.toLowerCase();
    const hasOverlap = secNorm.split(' ').some(w => w.length >= 5 && bodyNorm.includes(w));
    if (!hasOverlap) {
      body = `${body}, including ${secNorm}`;
    }
  }

  // Append purpose clause
  if (purposeClause) {
    body = `${body} ${purposeClause}`;
  }

  // Append continuity clause
  if (continuityClause) {
    body = `${body} — ${continuityClause}`;
  }

  return capitalizeFirst(body) + '.';
}

// Returns a secondary phrase from the second distinct title phrase (for description enrichment)
function getSecondaryPhrase(compressed, primarySubject) {
  const BROWSER_RE  = /^(chrome|firefox|safari|arc|brave|edge|opera)$/i;
  const AI_APP_RE   = /^(claude|chatgpt|gemini|copilot|perplexity|poe|phind)/i;
  const primaryNorm = (primarySubject || '').toLowerCase();

  for (const p of (compressed?.titlePhrases || []).slice(1, 5)) {
    if (p.isAITopic) continue;
    const { subject } = extractSubjectFromPhrase(p.phrase);
    const subNorm = subject.toLowerCase();
    if (
      subNorm.length >= 6 &&
      !isVagueSubject(subNorm) &&
      !isGenericSubject(subNorm) &&
      !BROWSER_RE.test(subNorm) &&
      !AI_APP_RE.test(subNorm) &&
      !isUtilityApp(subNorm) &&
      !primaryNorm.includes(subNorm.split(' ')[0]) &&
      !subNorm.includes(primaryNorm.split(' ')[0])
    ) {
      return capitalizeFirst(subject);
    }
  }
  return null;
}

// ─── Legacy alias kept for compatibility ─────────────────────────────────────
function buildDescriptionClauses(reasoning, titleSubject) {
  // Redirect to the new outcome-driven builder — returns a single assembled string
  // wrapped in an array so callers that iterate over clauses still work.
  return [buildOutcomeNarrative(reasoning, titleSubject)];
}

// ─── Title Generation ─────────────────────────────────────────────────────────

/**
 * Synthesize a high-quality, semantically compound title.
 * v2: Uses fusedWorkblock + ontology phrases first, falls back gracefully.
 */
function synthesizeTitle(reasoning, position = 0) {
  const { workMode, primaryCategory, project, intentResult } = reasoning;

  // Pick verb from the intent type for consistency — intentResult verb is more
  // specific than the generic workMode-based verb.
  const intentType = intentResult?.type || workMode?.replace('deep_', '') || 'implementing';
  const intentDef  = INTENT_TYPES[intentType] || INTENT_TYPES.implementing;
  const verbs      = intentDef.verbs || MODE_VERB_SETS[workMode] || MODE_VERB_SETS.deep_implementation;
  const usedVerbs  = reasoning._usedVerbs || [];
  const verb       = verbs[position % verbs.length] || pickVerb(workMode, position, usedVerbs);

  // Build compound subject from all available semantic sources
  const { subject, source, confidence } = buildCompoundSubject(reasoning);

  // ── Continuity shortcut — use "Continuing X" when session strongly continues ─
  if (reasoning.continuityProfile?.isContinuingWork &&
      reasoning.continuityProfile?.continuationTitleHint &&
      reasoning.continuityProfile.continuityConfidence >= 0.65) {
    const hint = reasoning.continuityProfile.continuationTitleHint;
    if (hint && isTitleAcceptable(hint)) return hint.slice(0, 72);
  }

  if (subject && confidence >= 0.5) {
    // Guard: reject all-generic subjects before building any title
    if (isGenericSubject(subject)) {
      // Fall through to graceful fallback below
    } else {
      // Already starts with a gerund verb? Use as-is to avoid double verbs.
      if (/^(implementing|building|designing|developing|engineering|debugging|researching|refactoring|reviewing|documenting|testing|planning|exploring|crafting|optimizing|analyzing)/i.test(subject)) {
        const candidate = dedup(capitalizeFirst(subject)).slice(0, 72);
        if (isTitleAcceptable(candidate)) return candidate;
      }

      // NEVER blindly prepend project name — only do it when subject is very short
      // and generic (≤2 meaningful words, no proper nouns from Title Case).
      const hasProperNounContent = (subject.match(/[A-Z][a-z]/g) || []).length >= 2;
      const wordCount = subject.split(/\s+/).length;
      const isShortGeneric = wordCount <= 2 && !hasProperNounContent && source !== 'ai_topic';

      let candidate;
      if (project?.name && isShortGeneric && !subject.toLowerCase().includes(project.name.toLowerCase())) {
        candidate = dedup(`${verb} ${project.name} ${subject}`).slice(0, 72);
      } else {
        candidate = dedup(`${verb} ${subject}`).slice(0, 72);
      }

      // Quality gate: only return if not auto-rejected
      const { rejected } = checkTitleRejectPatterns(candidate);
      if (!rejected) return candidate;
    }
  }

  // Graceful fallbacks — produce semantic titles, not just project names
  if (project?.name) {
    const domainHint = inferTitleDomainFallback(reasoning);
    return domainHint
      ? dedup(`${verb} ${domainHint}`).slice(0, 72)
      : dedup(`${verb} ${project.name}`).slice(0, 72);
  }

  const categoryLabels = {
    development: 'Feature Development',
    design:      'UI Design System',
    writing:     'Technical Documentation',
    research:    'Technical Research',
    planning:    'Architecture Planning',
    meeting:     'Team Sync',
  };
  return `${verb} ${categoryLabels[primaryCategory] || 'Product Development'}`;
}

// When no good subject is found, infer a semantic domain label from keywords
function inferTitleDomainFallback(reasoning) {
  const keywords   = reasoning.compressed?.keywords || [];
  const intentType = reasoning.intentResult?.type || 'implementing';
  const category   = reasoning.primaryCategory || 'development';

  const DOMAIN_ELEVATIONS = {
    burnout: 'Burnout Risk Intelligence',     fatigue: 'Fatigue Detection Systems',
    calendar: 'Calendar Intelligence',        scheduling: 'Scheduling Intelligence',
    analytics: 'Analytics Pipeline',          insight: 'Productivity Insights',
    session: 'Session Intelligence',          tracking: 'Activity Tracking',
    'ai': 'AI Intelligence Systems',         reasoning: 'Contextual Reasoning',
    semantic: 'Semantic Intelligence',        narrative: 'Narrative Synthesis',
    workflow: 'Workflow Intelligence',        continuity: 'Continuity Intelligence',
    design: 'Design System',                  component: 'UI Component Architecture',
    performance: 'Performance Optimization', database: 'Data Layer',
    authentication: 'Authentication System', notification: 'Notification System',
    report: 'Reporting Pipeline',
  };

  for (const kw of keywords) {
    for (const [term, elevation] of Object.entries(DOMAIN_ELEVATIONS)) {
      if (kw.toLowerCase().includes(term)) return elevation;
    }
  }
  return null;
}

