// ─── Utility / OS Apps ────────────────────────────────────────────────────────
// These apps are system tools — never meaningful work subjects or "tools used"
const UTILITY_APP_RE = /^(snipping\s*tool|snip\s*&\s*sketch|screenshot|photos|camera|calculator|notepad|wordpad|paint|paint\s*3d|windows\s*explorer|file\s*explorer|finder|activity\s*monitor|task\s*manager|system\s*preferences|settings|control\s*panel|system\s*info|disk\s*utility|magnifier|sticky\s*notes|clock|calendar\s*app|weather|maps|mail\s*app|news|microsoft\s*store|app\s*store|spotlight|cortana|siri|widgets|action\s*center)$/i;

function isUtilityApp(appName = '') {
  return UTILITY_APP_RE.test(appName.trim());
}

// ─── Vague Subject Guard ──────────────────────────────────────────────────────
// Subjects that read as generic / low-information
const VAGUE_SUBJECT_WORDS = new Set([
  'functions', 'things', 'stuff', 'items', 'content', 'data', 'information',
  'task', 'tasks', 'work', 'session', 'activity', 'feature', 'features',
  // App/route labels that must never become title subjects
  'productivity', 'dashboard', 'analytics', 'application', 'app',
  'platform', 'workspace', 'management', 'overview', 'summary',
  'settings', 'configuration', 'system', 'module', 'interface',
]);

function isVagueSubject(subject = '') {
  if (!subject || subject.length < 4) return true;
  const words = subject.toLowerCase().split(/\s+/);
  // All words are vague
  if (words.every(w => VAGUE_SUBJECT_WORDS.has(w))) return true;
  // Ends with a very generic word ("App Functions", "App Tasks")
  const lastWord = words[words.length - 1];
  if (VAGUE_SUBJECT_WORDS.has(lastWord) && words.length <= 3) return true;
  return false;
}

// ─── Title Phrase Bank ────────────────────────────────────────────────────────
// Varied, non-repetitive phrase structures — each used contextually.

const TITLE_STRUCTURES = {
  // verb + subject (most common)
  verb_subject: (verb, subject) => `${verb} ${subject}`,
  // subject + context
  subject_context: (subject, context) => `${subject} — ${context}`,
  // Gerund phrase
  gerund: (verb, subject) => `${verb} ${subject}`,
  // Noun phrase (no leading verb)
  noun_phrase: (subject) => subject,
};

